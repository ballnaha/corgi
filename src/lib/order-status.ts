// ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡πÑ‡∏î‡πâ‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
export function canTransitionTo(currentStatus: OrderStatus, newStatus: OrderStatus): boolean {
  const allowedTransitions = STATUS_TRANSITIONS[currentStatus];
  return allowedTransitions.includes(newStatus);
}

// ‡∏£‡∏±‡∏ö‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏ñ‡∏±‡∏î‡πÑ‡∏õ‡∏ó‡∏µ‡πà‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥
export function getNextRecommendedStatus(
  currentStatus: OrderStatus, 
  orderDetails: {
    requiresDeposit: boolean;
    shippingMethod: string;
    paymentType: string;
  }
): OrderStatus | null {
  const { requiresDeposit, shippingMethod, paymentType } = orderDetails;
  
  switch (currentStatus) {
    case "PENDING":
      if (requiresDeposit && paymentType === "DEPOSIT_PAYMENT") {
        return "PAYMENT_PENDING";
      }
      return "CONFIRMED";
      
    case "PAYMENT_PENDING":
      return "CONFIRMED";
      
    case "CONFIRMED":
      return "PROCESSING";
      
    case "PROCESSING":
      return "SHIPPED";
        
    case "SHIPPED":
      return "DELIVERED";
      
    default:
      return null;
  }
}

